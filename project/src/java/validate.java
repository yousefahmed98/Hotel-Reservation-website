    /**
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code>
     * methods.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
     protected void processRequest(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException, ClassNotFoundException, SQLException {
        response.setContentType("text/html;charset=UTF-8");
        try (PrintWriter out = response.getWriter()) {
            Class.forName("com.mysql.jdbc.Driver");
            String url = "jdbc:mysql://localhost:3305/hotel_reservation_database";
            String user = "root";
            String databasePassword = "12345678";
            Connection Con = null;
            PreparedStatement Stmt = null;
            Con = DriverManager.getConnection(url, user, databasePassword);
            HttpSession session = request.getSession(true);

            Stmt = Con.prepareStatement("SELECT * FROM hotel_reservation_database.users WHERE username = ? AND password = ? ;");

            String username = request.getParameter("username");
            String password = request.getParameter("password");
            Stmt.setString(1,username);
            Stmt.setString(2,password);
            ResultSet RS = Stmt.executeQuery();

            if(!RS.next() ){

                response.sendRedirect("login.html");
                
            }
            else{
                session.setAttribute("session_username", username);
                response.sendRedirect("customerhome.jsp‚Äù");
            }
            RS.close();
            Stmt.close();
            Con.close();

        }
        catch (Exception ex) {
                ex.printStackTrace();
                out.print(ex);
        }
       
    }

    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
